// Good problem on graphs
// So, how do we solve this one?
// Lets make some observations.
// The problem itself is kind hard to understand with all the notations.
// But, the idea is simple.
// We are given n distinct pairs and we wanna find the maximum of
// f(S') - g(S'), where S' is a subset of S. 
// f(S) = the union of all the pairs of number we took.
// ie. S = {(1,4), (3,6), (2,10)} => f(S) = 10, because the union of all
// the pairs is 10.
// g(S) = the cycle length of the component (which is at least 3).
// ie. S = {(1,4), (4,6), (6,1)} => g(S) = 3 because 1<->4<->6<->1 is a cycle.
// And thats the idea.
// So, how do we solve this?
// First, lets make a crucial observation.
// Notice that, actually we can represent this as graph problem.
// Now, this is actually just a bunch of disjoin components that may or may
// not have cycles in them. Note these cycles are the ones that will contribute
// to g(S') in the end.
// We wanna maximize f(S') - g(S'). How do we do this?
// Think greedily.
// Well, if we look at any disjoint component, what is the f(S') of this 
// component? Then answer is always the difference in min and max node of the 
// component + 1, eg. mxNode - mnNode + 1 for this component's contribution
// to f(S').
// And then, obviously we minus all the cycle component, which is g(S').
// But, realize an important observation, do we really need to minus g(S')?
// Actually, no. Why? Becuase since we wanna maximize f(S') - g(S'), then
// we can simply just pick S' such that we never form a cycle!
// This way, we never make a cycle and so we will never make g(S') > 0.
// Notice that f(S) = f(S') still even after doing this.
// Why? Because are nodes in the component still remain, we are just removing
// redundant edges that form a cycle and contribute to g(S'), which is not 
// ideal.
// So, actually we can just ignore cycles, and sum every disjoint component
// this way.
// In other words, the answer is literally just f(S) = f(S') - g(S').
// Which means, we can just dfs into each disjoint component, and add the 
// edges for each component, when we encounter a redundant edge (edge that
// form a cycle in our current component), we simply ignore it.
// And thats the solution.

vi result;
map<pii, int> memo;

void dfs(int curNode, vb& visited, vvi& adjList){
    visited[curNode] = true;
    for (int nextNode: adjList[curNode]){
        if (!visited[nextNode]){
            result.pb(memo[{curNode, nextNode}]);
            dfs(nextNode, visited, adjList);
        }
    }
}


