// solve time ~ 1hr
// Yea this is just bs, but the implementation is annoying.
// Also, the problem is so fucking long and hard to understand cuz
// its so long. Took me like 10 mins to understand what they wanted.
// But yea once I realized it was quite easy to solve conceptually.
// Implementing it was a bit of pain.
// The idea it to do process of elimination via bs.
// Why? Notice that each column is sorted, because bitwise OR
// never decrease the value of our next row when we OR with more
// elements.
// Hence, when you look column by column, its actually non-decreasing.
// Now, for every query, what they essentially want is to find
// rows at specific columns such that it fufills their condition.
// So, if we want r > c, then it means we want all the rows
// where at column r their values are > x. Meaning, we can just do ub
// on c, and retrive the index, which indicates all valid rows
// are >= this row we found. 
// Likewise, if they want r < c, then we can just do lb on c then -1
// on the index because they want stricly less than c.
// Meaning, all rows that are valid are <= the row we found.
// All of this can be done with bs.
// But, there is another thing to note.
// We need to always do min(low, our new ub) if < and 
// max(high, our new lb) because if lets say previously we had a 
// condition where low >= 3 from some region, then now we found
// that low >= 1 for another region, then the stronger condition 
// is low >= 3 because we need to satisfy both conditions.
// The same applies for high.
// Then, we just return low in the end, unless low > high, in which
// case its -1 because there is no row that is valid.

void solve() {
    int n, m, k, q;
    cin >> n >> k >> q;

    vvi a(n+1, vi(k+1, 0));
    for (int i=1; i<=n; i++){
        for (int j=1; j<=k; j++){
            cin >> a[i][j];
        }
    }

    // bitwise OR the rows 
    for (int j=1; j<=k; j++){
        for (int i=2; i<=n; i++) a[i][j] |= a[i-1][j];
    }
    
    // Transpose the array for bs.
    vvi b(k+1, vi(n+1));
    for (int j=1; j<=k; j++){
        for (int i=1; i<=n; i++) b[j][i] = a[i][j];
    }

    while(q--){
        int low = 1, high = n;
        cin >> m;
        while (m--){
            int r, c;
            ch o;
            cin >> r >> o >> c;
            if (o == '>'){
                auto it = ub(b[r].begin()+1, b[r].end(), c);
                low = max(low, int(it - b[r].begin()));
            } 
            else{
                auto it = lb(b[r].begin()+1, b[r].end(), c);
                int pos = int(it - b[r].begin()) - 1;
                high = min(high, pos);
            }
        }

        cout << (low > high ? -1 : low) << '\n';
    }

}

