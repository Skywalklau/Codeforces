// solve time ~ 30mins
// Nice problem, I took inspiration from Dr Evil Underscores to solve 
// this one.
// Note:
// a[i] & a[j] >= a[i] ^ a[j] <=> At some higher bit position,
// we have 1 & 1 == 1 >= 1 ^ 1 == 0.
// Lets think bit by bit,
// we can group each a[i] into 2 separate groups l and r.
// group l contains numbers with the current bit pos set as 0
// group r contains numbers with the current bit pos set as 1
// Now, within the group of r, there is sz(r)C2 ways to form valid pairs
// because since the MSB has 1 & 1 == 1, and 1 ^ 1 == 0. No matter what,
// these pairs of number are automatically gonna satisfy the condition.
// We don't need to look into their smaller bit positions.
// But, for the group l, its different, because 0 & 0 == 0 >= 0 ^ 0 == 0
// The issue is that, later on at a smaller bit position there could
// be the case where its 1 & 0 == 0 < 1 ^ 0 == 1.
// Meaning among the pairs of l, just because at this bit position its ok
// it does not mean at a smaller bit its ok, in fact it might be wrong.
// So, we cannot conclude they are valid pairs yet.
// Therefore, we need to check their lower bit positions.
// Also, notice that we can never choose between the pair of values,
// one of each from both sets.
// Why? Because at this bit position we can already conclude that 
// the AND will be 0, but the XOR will be 1 at this bit position.
// So, no matter at the later bit position the pairs a[i] and a[j] 
// in different sets will never satisfy the condition.
// eg. 
// a[i] == (1)00101
// a[j] == (0)10101
// at (), notice that no matter what, a[i] & a[j] < a[i] ^ a[j].
// So we don't need to pick among those pairs.
// Hence the intuition for divide and conquer comes in.
// we split into l and r.
// among the group r, we can do sz(r)C2, then we continue down a lower
// bit position on l, and repeat the same process.
// We can use dfs.


ll dfs(vi& a, int bit){
    if (bit < 0) return 0;
    vi l, r;

    for (int i=0; i<sz(a); i++){
        if ((a[i] >> bit) & 1) r.pb(a[i]);
        else l.pb(a[i]);
    }

    return nCr_no_mod(sz(r), 2) + dfs(l, bit-1);

}


