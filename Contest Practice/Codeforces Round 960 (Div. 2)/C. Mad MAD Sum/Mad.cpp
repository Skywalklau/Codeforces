// First, lets use the MAD operation on each element once.
// So we add the initial sum of a to our ans.
// we get a new array, and lets the MAD operation again and again.
// You will notice that actually the elements are shifted right
// each turn.
// So the idea is we can just add the sum of contribution of each
// number after the first operation using index i from n.
// HOWEVER, there is an issue, if we perform just one operation,
// we could end up having something like this:
// [0,0,...,1,2,3,4,4,4,5,5,5,6]
// Notice that after the next move, the array becomes:
// [0,0,...,0,0,0,0,4,4,4,5,5,5]
// Notice something? For numbers that only have 1 occurence, they
// will just be gone in 2nd move (they will not be shifted right).
// But, notice that after the 2nd move onwards, we are always safe
// to just perform the initial idea (because every element now) will
// have a duplicate next to it until a better element comes next.
// I missed this observation at first, but I figured out later on.
// In other words, we need to simulate the first 2 moves first.
// then from the 2nd move onwards we can just use our initial trick.
// So, add the sum of a initially, then operate once then add the 
// new array again, then operate again.
// Then with this new array we can use our trick to count the 
// contribution of each number from n.

void change(vll&a, int n){
    map<ll, ll> freq;
    vll b(n);
    ll curMax = 0;
    for (int i=0; i<n; i++){
        freq[a[i]]++;
        if (freq[a[i]] >= 2) curMax = max(curMax, a[i]);
        a[i] = curMax;
    }
}

