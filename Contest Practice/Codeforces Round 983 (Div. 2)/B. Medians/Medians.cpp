// So the trick to solving this problem is to notice that we can just cheese it.
// the answer is always just using 3 intervals.
// My intuition told me so, and I was right.
// We are given that n is odd.
// Also, the condition given is that each subarray must be odd as well.
// so we aim to achieve odd + odd + odd = odd.
// or k1 + k2 + k3 = n.
// where everything is odd.
// Can we achieve this? Yes.
// The trick to doing this is that sz(k2) is either 1 or 3.
// By default, we always just set sz(k2) to 1, which is contains k itself.
// lets say we have [1,2,3,4,5]
// we want median to be k=4.
// then we can just do [1,2,3], [4], [5].
// Notice something? If k == 1 or k == n, its impossible.
// Because again we wanna split it into m groups that are ODD.
// We can never achieve this.
// Unless, k == n == 1. Which is an edge case.
// Otherwise, we always wanna try to cheese it into m=3 groups of odd sizes 
// We just wanna make sure the median(k2) == k, so that median(k1,k2,k3) == k.
// The way we can do this is to make sure k1 and k3 are odd as well.
// Because there will be a case where k1 is even, k2 is odd, then k3 is even.
// Btw, we will never have k1 even then k3 odd or vice versa.
// Because n is odd, so implicitly if k1 is even, then k3 MUST be even and 
// vice versa. Likewise, k1 odd <=> k3 odd.
// Eg. [1,2,3,4,5,6,7], k=5
// then [1,2,3,4], [5], [6,7]. This is wrong because k1 and k3 are even.
// So, this is where k1 needs to be 3 instead of 1.
// We will make it [1,2,3], [4,5,6], [7].
// This way the median of k2 is still 5. Which is what we want.
// And thats the answer.
// Then we just need to print the boundaries correctly and thats it.

