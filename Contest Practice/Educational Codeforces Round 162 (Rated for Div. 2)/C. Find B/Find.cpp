// The trick is notice that we can cheese this shit.
// Realize that if a[i] != 1, we can always bring it down to 1.
// In other words, lets say we have [2,3,3,4]
// We can bring all these elements to 1 to make it [1,1,1,1],
// obviously now we have an extraSum that we need to use, what to do 
// with it? Just put it at some element, this guarantees that
// all elements will not be equal to a[i] for any i, and the sum is 
// still equal.
// But, notice that we cannot cheese this if a[i] == 1.So, do what?
// We make it into an element > 1 loh. 
// Lets say we have [1,23,3,2,1]
// First, make it all to 1, then the subarray becomes [1,1,1,1,1]
// we will have an extra sum of sum(all(subarray)) - 5, where 5
// is just the size of the subarray since we have 5 elements of 1.
// then, if we know the number of 1's in this subarray (which we can
// easily get by prefix sums).
// then, by pigeonhole principle, as long as we have extra sum > number
// of 1's in the subarray, we can always make these 1 elements into
// elements > 1.
// In other words, for non 1 elements, we make them 1.
// With the extra sum remaining after changing elements > 1 into 1,
// we check, if the remaining extra sum > number of original 1 in the 
// subarray, we can always form a valid subarray according to the 
// conditions.
// if the array has no 1 in the first place, then its always possible.
// There is an edge case, if the range l to r is just 1, meaning its
// just 1 elements, then its always impossible because we can
// never have the same sum while using a value different from a[i] itself.

