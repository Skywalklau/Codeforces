// The trick to solving this problem is to notice that because
// we can only pick cards that are consecutive.
// This implies we cannot pick some subseq. of numbers.
// => dp seems unreasonable.
// The question now just becomes:
// What is the largest window of elements that are consecutive
// that I can pick, where each window can only have at most k 
// distinct elements.
// Which suggests sliding window.
// So, proceed with sliding window, we just wanna take the largest
// interval for each starting point i.
// the concept is simple, we start from i, find the largest j
// such that each element in consecutive, and j-i+1<=k.
// until the moment, j >= n or j-i+1>k or a[j] != a[j-1]+1.
// This implies we need to find a new interval.
// So we start at a new i again, and repeat the process.
// Note: we don't need to reset j, because implicitly
// if a[i] to a[j>i] is valid, then a[i+1] to a[j>i] is also valid.
// However, if j<=i (because there is no valid consecutive number
// for this current a[i]), then we just need to do j=i+1 and repeat
// the same 2 pointer logic again.

