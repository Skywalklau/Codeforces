// solve time ~ 20 mins
// Nice state compression problem with dp.
// Personally, I solved everything correctly but my dp was O(N^2).
// Why? First let us discuss the dp itself.
// let dp[i] = max number of removals up to the ith ball so far.
// base case: dp[0] = 0, with no balls, there are 0 removals.
// Transition:
// dp[i] = max(dp[i-1], dp[j-1] + (i-j+1))
// dp[i-1] => don't remove this ball
// dp[j-1] + (i-j+1) => remove the ith ball with the jth ball where
// a[i] == a[j].
// However, to do this, we would need to do this in O(N^2) because we
// would need a nested loop to check if a[i] == a[j].
// This is where I fell short.
// Notice that actually dp[j-1] + (i-j+1) == i + dp[j-1] - (j-1).
// And so we will go backwards to check if a[i] == a[j] and do our 
// dp transition of dp[i] = max(dp[i-1], i + dp[j-1] - (j-1)).
// But, notice that this just means we are just trying to find the best 
// dp[j-1] - (j-1) up to i so far.
// In this case, we want the max of dp[j-1] - (j-1).
// In other words, we can just keep track of the maximum of dp[j-1] - (j-1).
// for each a[j].
// This way, we don't have to do a nested for loop to check every element
// if a[i] == a[j], we can just straightaway take the best dp[j-1] - (j-1).
// and use it with i.
// So, the transition becomes:
// dp[i] = max(dp[i-1], i + mx[a[i]]).
// where mx[a[i]] stores the best dp[j-1] - (j-1) so far, before computing
// for dp[i-1] - (i-1) itself.

// Finally, we can return dp[n].
// Time complexity: O(N)
// Space complexity: O(N)

void solve() {
	int n, k, m;
    cin >> n;

    vi a(n+1);
    for (int i=1; i<=n; i++) cin >> a[i];
    
    vi dp(n+1, -MAX), mx(n+1, -MAX);
    dp[0] = 0;
    for (int i=1; i<=n; i++){
        dp[i] = max(dp[i-1], i + mx[a[i]]);
        mx[a[i]] = max(mx[a[i]], dp[i-1] - (i-1));
    }

    cout << dp[n] << '\n';
}

