// Nice problem, I initially thought this was a trie problem.
// But you can't do that...
// even if we did on the fly, it cannot work.
// even if we did via sliding window, it cant work as well
// because the main point is, we are always using the max and 2nd max
// element.
// So what does this tell us?
// within a certain window, no matter how we extend the index
// to expand it, if the 2nd largest and largest element is 
// still the same then it is always redundant to do so.
// in other words, example:
// [9, 8, 3, 5, 7]
// we first have [9,8].
// then we expand to [9,8,3]
// notice that, 3 does not change anything up to i=2 so far.
// we only need to check for [8,3], everything behind it (if there is)
// is redundant to check because there will be an element > 3, so the
// XOR will not include 3.
// then we move to [9,8,3,5]
// notice that, we up to 5, we can check [3,5], and [8,3,5].
// because from [...,9] and backwards (if there are more elements)
// anything after that 9 => 5 will never be used anymore.
// then moving to [9,8,3,5,7]
// same thing, we only need to check up till [5,7],[3,5,7],and [8,3,5,7].
// anything from [...,9] and backwards (if there are more elements)
// is redundant to check, because 7 will never be used anymore in the xor.
// notice a pattern?
// there is a monotonic decreasing structure.
// => monotonic decreasing stack.
// if st.top() > a[i], where a[i] is the current element we are checking
// then => we only need to check for [st.top(), a[i]] because 
// everything backwards of [st.top()] will never use a[i] for the xor 
// already.
// otherwise, if st.top() < a[i],
// we do a while loop, while st.top() < a[i]
// we check each [st.top(), a[i]].
// until st.top() >= a[i].
// visually, [5,2,1,4,3]
// at i=3, a[i]=4, we need to check [5,2,1,4], [2,1,4], [1,4].
// => we pop until 5, because 5 >= 4.

void solve() {
	int n, k, m;
    cin >> n;

    vi a(n);

    for (int& x: a) cin >> x;
    
    stack<int> st;
    int result = 0;
    for (int i=0; i<n; i++){
        while (!st.empty() && st.top() < a[i]){
            result = max(result, st.top() ^ a[i]);
            st.pop();
        }
        
        if (!st.empty() && st.top() > a[i]) result = max(result, st.top() ^ a[i]);
        st.push(a[i]);
    }

    cout << result << '\n';

}

