// Very nice problem, learned a new trick!
// Could not solve, Thought it was just Kruskall but that would be O(n^2)
// cuz we would need all the edges for that.
// Got stuck on how to deal with 0 edges.
// Turns out the trick is to use dsu and compare, for each node i, if 
// the number of 1-weight connections to a component < the size of a 
// component, then this implies that there must be at least one zero-
// edge connection between i and this component, so we can merge.
// Keep doing this, and we end up with k connected components where the 
// these k components are all the connections made only using 0-weight
// edges. Since we already greedily connected all the k components 
// using 0-weight edges, as we only want one connected component, the only 
// way to do this is by using 1-weight edges, and we have to do this k-1
// times (because we have k connected components, so we need k-1 more
// edges to connected everything, note that the graph is a clique).

// Time complexity: O(n)
// Space complexity: O(m + n*log(n))

int parent[MAXN], sz[MAXN];

int find(int node){
    if (parent[node] == node) return node;
    return parent[node] = find(parent[node]);
}

void unite(int u, int v){
    u = find(u), v = find(v);
    if (u != v){
        if (sz[u] < sz[v]) swap(u, v);
        parent[v] = u;
        sz[u] += sz[v];
    }
}

void solve() {
	int n, k, m;
    cin >> n >> m;

    for (int i=1; i<=n; i++){
        parent[i] = i;
        sz[i] = 1;
    }

    vvi adjList(n+1);

    for (int i=0; i<m; i++){
        int node1, node2;
        cin >> node1 >> node2;
        adjList[node1].pb(node2);
        adjList[node2].pb(node1);
    }

    set<int> comp; // stores connected components so far
    for (int node=1; node<=n; node++){
        // count stores for each connected component so far, the number of
        // 1-weight edges from node i to each component.
        map<int, int> count;
        for (int nextNode : adjList[node]) count[find(nextNode)]++;
        for (int rootNode : comp){
            // if the number of connections to this component with node i
            // is < the size of the component
            // => at least one 0-edge exist, so we take that edge to this
            // component
            if (count[find(rootNode)] < sz[find(rootNode)]){
                unite(find(rootNode), node);
            }
        }

        if (find(node) == node) comp.insert(node); // new component 
    }

    int ans = 0;
    for (int i=1; i<=n; i++) if (find(i) == i) ans++;

    // we need k-1 more 1-edge connections, so total weight needed == k-1.
	cout << ans - 1 << '\n';

}

